function sanitizeData() {
  const dataToBeSanitized = data;

  const dataGroupedByIncome = [];

  for (const item of dataToBeSanitized) {
    const selectedIncome = item.renda;

    const selectedIncomeFound = dataGroupedByIncome.find(
      (el) => el.renda === selectedIncome
    );

    if (!!selectedIncomeFound) {
      continue;
    }

    const dataWithThatIncome = dataToBeSanitized.filter(
      (el) => el.renda === selectedIncome
    );

    let payload = {
      renda: selectedIncome,
      data: [],
    };

    dataWithThatIncome.forEach((el) =>
      payload.data.push({
        quantidade_ocorrencias: el.quantidade_ocorrencias,
        escolaridade: el.escolaridade,
        mes_reposta: el.mes_reposta,
      })
    );

    dataGroupedByIncome.push(payload);
  }

  return dataGroupedByIncome;
}
